/**
 * SVGIcon — Renders any icon from the ICONS library.
 *
 * @param {string}  name       - Icon name from ICONS object (required)
 * @param {number}  size       - Icon size in pixels (default: 20)
 * @param {string}  color      - CSS color value (default: 'currentColor')
 * @param {string}  className  - Additional CSS classes
 * @param {string}  ariaLabel  - Accessibility label (required for standalone icons)
 * @param {boolean} ariaHidden - Hide from screen readers (use when icon is decorative)
 * @param {object}  style      - Inline style overrides
 */
const SVGIcon = ({
  name,                    // Icon name — must match a key in ICONS
  size = 20,               // Default 20px size
  color = 'currentColor',  // Inherits text color by default
  className = '',          // Additional CSS classes
  ariaLabel,               // Screen reader label
  ariaHidden,              // True if purely decorative
  style = {},              // Inline style overrides
  ...props                 // Any other HTML attributes (onClick, etc.)
}) => {

  // Look up the SVG element for the requested icon name
  const icon = ICONS[name];

  // If icon not found, log a warning and render a fallback
  if (!icon) {
    console.warn(`[FlashCart SVGIcon] Icon "${name}" not found in icon library.`);
    // Return a simple square placeholder for missing icons
    return (
      <span
        className={`fc-icon fc-icon-missing ${className}`}
        style={{
          display: 'inline-block',
          width: `${size}px`,
          height: `${size}px`,
          background: '#E5E7EB',
          borderRadius: '4px',
          ...style,
        }}
        aria-hidden="true"
      />
    );
  }

  // Determine accessibility attributes
  // If ariaHidden is explicitly set, use it
  // If ariaLabel is provided, icon is not hidden
  // If neither, hide from screen readers (treat as decorative)
  const accessibilityProps = ariaHidden === false || ariaLabel
    ? {
        role: 'img',                       // Semantic role for screen readers
        'aria-label': ariaLabel,           // Descriptive label
        'aria-hidden': undefined,          // Not hidden
      }
    : {
        'aria-hidden': 'true',             // Hidden — decorative only
        role: undefined,
      };

  // Clone the SVG element with the required props
  // React.cloneElement preserves the SVG structure and adds new props
  return React.cloneElement(icon, {
    // Accessibility
    ...accessibilityProps,

    // Sizing — explicit width/height for crisp rendering
    width: size,
    height: size,

    // Color — CSS color property inherited from parent or explicit
    color: color,

    // CSS class for any additional styling
    className: `fc-icon ${className}`.trim(),

    // Inline styles — size + any overrides
    style: {
      display: 'inline-block',     // Consistent display
      verticalAlign: 'middle',      // Aligns with text
      flexShrink: 0,                // Never shrink in flex containers
      color: color,                 // CSS color (used by currentColor in SVG)
      width: `${size}px`,          // Explicit pixel size
      height: `${size}px`,
      ...style,                     // User-provided style overrides
    },

    // Pass through any additional props (event handlers, data attributes)
    ...props,
  });
};

// Export SVGIcon as default
export default SVGIcon;

